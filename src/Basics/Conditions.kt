
fun main() {

    var price=5

    //If Condition

    if (price>=10){
        price++
        println(price)
    }
    else if(price<=5){
        price =10
        println(price)
    }
    else{
        price--
        println(price)
    }

    // If Single Line

    var catNum = 3
    if (catNum>=3) println(-catNum) else println(--catNum)

}